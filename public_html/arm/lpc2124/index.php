                 • Two 32-bit timers (with 4 capture and 4 compare channels), PWM unit (6 outputs), Real Time Clock and Watchdog.<br/><br/>
                 • Multiple serial interfaces including two UARTs (16C550), Fast I2C (400 kbits/s) and two SPIs™.<br/><br/>
                 • 60 MHz maximum CPU clock available from programmable on-chip Phase-Locked Loop.<br/><br/>
                 • Vectored Interrupt Controller with configurable priorities and vector addresses.<br/><br/>
                 • Up to forty-six (64 pin) and hundred-twelve (144 pin package) 5 V tolerant general purpose I/O pins. <br/>
                  &nbsp;&nbsp;&nbsp;Up to 12 independent external interrupt pins available (EIN and CAP functions).<br/><br/>
                 • On-chip crystal oscillator with an operating range of 1 MHz to 30 MHz.<br/><br/>
                 • Two low power modes, Idle and Power-down. <br/><br/>
                 • Processor wake-up from Power-down mode via external interrupt.<br/><br/>
                 • Individual enable/disable of peripheral functions for power optimization.<br/><br/>
                 • Dual power supply. <br/>
                  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- CPU operating voltage range of 1.65V to 1.95V (1.8V +/- 8.3%).<br/>
                  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- I/O power supply range of 3.0V to 3.6V (3.3V +/- 10%).<br/><br/>
