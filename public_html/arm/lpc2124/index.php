             <p><Strong>GENERAL DESCRIPTION </Strong></p>
    		     <p class="C1p1">



                 The LPC2114/2124/2212/2214 are based on a 16/32 bit ARM7TDMI-STM CPU with real-time emulation and embedded trace support, together with 128/256 kilobytes (kB) of embedded high speed flash memory.
                 A 128-bit wide internal memory interface and a unique accelerator architecture enable 32-bit code execution at maximum clock rate.<br/>
                 For critical code size applications, the alternative 16-bit Thumb Mode reduces code by more than 30% with minimal performance penalty.
