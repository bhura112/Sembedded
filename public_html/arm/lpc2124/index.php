                 <p><Strong> FEATURES </Strong></p>
                 • 16/32-bit ARM7TDMI-S microcontroller in a 64 or 144 pin package.<br/><br/>
                 • 16 kB on-chip Static RAM<br/><br/>
                 • 128/256 kB on-chip Flash Program Memory (at least 10,000 erate/write cycles over the whole <br/>
                   &nbsp;&nbsp;&nbsp;temperature range). 128-bit wide interface/accelerator enables high speed 60 MHz operation.<br/><br/>
                 • External 8, 16 or 32-bit bus (144 pin package only)<br/><br/>
                 • In-System Programming (ISP) and In-Application Programming (IAP) via on-chip boot-loader software. <br/>
                   &nbsp;&nbsp;&nbsp;Flash programming takes 1 ms per 512 byte line. Single sector or full chip erase takes 400 ms.<br/><br/>
                 • EmbeddedICE-RT interface enables breakpoints and watch points.Interrupt service routines can  <br/>
                   &nbsp;&nbsp;&nbsp;continue to execute whilst the foreground task is debugged with the on-chip RealMonitor software.<br/><br/>
                 • Embedded Trace Macrocell enables non-intrusive high speed real-time tracing of instruction execution.<br/><br/>
                 • Four/eight  channel (64/144 pin package) 10-bit A/D converter with conversion time as low as 2.44 ms.<br/><br/>
